package com.contacts.sheet.model;

import lombok.Data;

@Data
public class Metric {
    private String name;
    private Long value;

    // Getters and Setters
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getValue() {
        return value;
    }

    public void setValue(Long value) {
        this.value = value;
    }
}
